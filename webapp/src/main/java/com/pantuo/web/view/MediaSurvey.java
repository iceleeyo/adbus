package com.pantuo.web.view;

public class MediaSurvey {
   String locationstr; //位置
   String personnum;   //人次
   String linesurvey;   //线路概况
   String mesurvey;    //媒体概况
   String mediaowner;   //媒体属主
   String mediacount;   //媒体数量
public String getLocationstr() {
	return locationstr;
}
public void setLocationstr(String locationstr) {
	this.locationstr = locationstr;
}
public String getPersonnum() {
	return personnum;
}
public void setPersonnum(String personnum) {
	this.personnum = personnum;
}
public String getLinesurvey() {
	return linesurvey;
}
public void setLinesurvey(String linesurvey) {
	this.linesurvey = linesurvey;
}
public String getMesurvey() {
	return mesurvey;
}
public void setMesurvey(String mesurvey) {
	this.mesurvey = mesurvey;
}
public String getMediaowner() {
	return mediaowner;
}
public void setMediaowner(String mediaowner) {
	this.mediaowner = mediaowner;
}
public String getMediacount() {
	return mediacount;
}
public void setMediacount(String mediacount) {
	this.mediacount = mediacount;
}
   
   
}
