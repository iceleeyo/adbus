package com.pantuo.web.view;

public class ContractLineDayInfo {
	
	public int lineid;
	public int totalBus;
	public int dayOnlieBus;
	public int getLineid() {
		return lineid;
	}
	public void setLineid(int lineid) {
		this.lineid = lineid;
	}
	public int getTotalBus() {
		return totalBus;
	}
	public void setTotalBus(int totalBus) {
		this.totalBus = totalBus;
	}
	public int getDayOnlieBus() {
		return dayOnlieBus;
	}
	public void setDayOnlieBus(int dayOnlieBus) {
		this.dayOnlieBus = dayOnlieBus;
	}
	
	
	

}
